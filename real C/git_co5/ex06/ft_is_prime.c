int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	i = 1;
	if (nb % 2 == 0 && nb != 2)
		return (0);
	while (i <= (nb / 2))
	{
		if (nb % i == 0 && i != 1)
			return (0);
		else
			i += 2;
	}
	return (1);
}
