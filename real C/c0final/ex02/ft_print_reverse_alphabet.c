#include <unistd.h>

void	ft_print_char(char c)
{
	write (1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	c;

	c = 'z';
	while (c >= 'a')
	{
		ft_print_char(c);
		c--;
	}
}
